	<h1 class="mb-2 text-2xl">Hungrig Ankunft (Tag 1)</h1>
	<p class="text-left text-base leading-relaxed">
		<span class="mb-2 block"
			>Du schlägst die Augen auf und blinzelst gegen das helle Licht, das durch die Bäume bricht. Um
			dich herum wuseln unzählige kleine Elfen hektisch durcheinander, schleppen Körbe voller
			glänzender Zauberflocken und stoßen dabei immer wieder aneinander. Ein besonders kleiner Elf
			mit leuchtend grüner Mütze stolpert zu dir und stemmt die Arme in die Seiten.</span
		>

		<span class="mb-2 block"
			>„Na, wie kommt’s, dass du nur herumliegst?“ schimpft er und funkelt dich mit
			zusammengekniffenen Augen an. „Hier herrscht das reinste Chaos, und du hilfst überhaupt
			nicht!“</span
		>

		<span class="mb-2 block"
			>Du reibst dir die Augen und versuchst, deine Gedanken zu ordnen, doch in deinem Kopf ist nur
			gähnende Leere. „Äh… Entschuldigung? Ich kann mich irgendwie an gar nichts erinnern. Was ist
			hier los?“</span
		>

		<span class="mb-2 block"
			>Der Elf rollt die Augen, als sei das das Dümmste, was er je gehört hat. „Die Rentiere des
			Weihnachtsmanns brauchen dringend neues Futter! Das alte wurde gestohlen – keiner weiß, wer es
			war. Jedes Rentier braucht 200 Zauberflocken, und ohne die kann niemand in den Schlitten
			steigen und die Geschenke ausliefern!“</span
		>

		<span class="mb-2 block"
			>Langsam dämmert dir die Dringlichkeit der Situation, und du schaust dich um. Überall rennen
			die Elfen mit winzigen Körben hin und her, schaffen kaum nennenswerte Mengen an Flocken zur
			Rentierstation, und das ganze Treiben wirkt reichlich unkoordiniert.
		</span>

		<span class="mb-2 block"
			>Du atmest tief durch und wendest dich wieder an den Elf mit der grünen Mütze. „Okay, das muss
			besser gehen. Kannst du mir eine Übersicht der Rentierflotten geben? Dann verteilen wir die
			Elfen so, dass jedes Rentier das Futter in maximal zwei Gängen bekommt.“</span
		>

		<span class="mb-2 block"
			>Der Elf zieht eine Liste aus seiner Tasche und reicht sie dir. Sie zeigt die Anzahl der
			Rentiere in jeder Flotte an:</span
		>

		<span class="mt-2 block">Flotte Donner: 8 Rentiere</span>
		<span class="mb-4 block">Flotte Blitz: 3 Rentiere</span>
		<span class="mb-2 block"
			>„Jede Elfin und jeder Elf können bis zu 150 Zauberflocken tragen“ erklärt dir der Elf bevor
			er sich aufmacht, den anderen zu helfen</span
		>

		<span class="mb-2 block"
			>Du denkst kurz nach. Flotte Blitz benötigt insgesamt <b>600 Zauberflocken</b>. Das schaffen
			<b>2 Elfen</b> in zwei Runden. Für die Flotte Donner werden wohl <b>6 Elfen benötigt</b>.
		</span>
	</p>
